#!/bin/bash

start_line=`grep "$1" -ne '--------  --------  --------  --------  -------  -------  ------  ------  --------' | cut -d ':' -f 1`
end_line=`grep "$1" -ne '-------------------------------------------------------------------------------' | tail -n 2 | head -n 1 | cut -d ':' -f 1`

sed -n $((start_line+1)),$((end_line-1))p "$1" | awk '{print $1, $3, $7}' | tr -d ',' > "$1.dat"
